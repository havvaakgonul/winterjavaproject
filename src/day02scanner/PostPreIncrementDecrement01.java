package day02scanner;

public class PostPreIncrementDecrement01 {

	public static void main(String[] args) {
		
		int x = 12;
		System.out.println(x++);
		System.out.println(x);
		
		System.out.println(++x);
		
		System.out.println(x--);
		System.out.println(x);
		
		System.out.println(--x);
		System.out.println(x++);
		System.out.println(x = x + 1);
	}

}
